package com.ustcinfo.mobile.platform.data;

public class UserMapInfo {
	private String userAccount;
	private String mapUserAccout;
	private String appId;
	public String getUserAccount() {
		return userAccount;
	}
	public void setUserAccount(String userAccount) {
		this.userAccount = userAccount;
	}
	public String getMapUserAccout() {
		return mapUserAccout;
	}
	public void setMapUserAccout(String mapUserAccout) {
		this.mapUserAccout = mapUserAccout;
	}
	public String getAppId() {
		return appId;
	}
	public void setAppId(String appId) {
		this.appId = appId;
	}
}
